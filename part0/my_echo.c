#include <stdio.h> // אנחנו צריכים את זה בשביל פונקציית ההדפסה printf

/*
 * int main(int argc, char *argv[])
 *
 * argc: יכיל את מספר הארגומנטים (בדוגמה: 4)
 * argv: יהיה מערך המחרוזות (בדוגמה: ["./my_echo", "hello", "world", "123"])
 */
int main(int argc, char *argv[]) {
    
    // אנחנו רוצים להתחיל להדפיס מהארגומנט האמיתי הראשון,
    // שהוא תמיד במקום 1 (כי מקום 0 זה שם התוכנית).
    // לכן, נתחיל את הלולאה מ- i = 1
    //
    // נמשיך את הלולאה כל עוד i קטן מ- argc (כלומר, קטן מ-4).
    // זה אומר שהלולאה תרוץ עבור i=1, i=2, ו- i=3.
    for (int i = 1; i < argc; i++) {
        
        // הדפס את המחרוזת שנמצאת במקום ה-i במערך argv
        // %s הוא "ממלא מקום" למחרוזת
        printf("%s", argv[i]);
        
        // הדרישה היא להדפיס רווח *בין* המילים.
        // אנחנו לא רוצים רווח אחרי המילה האחרונה.
        // לכן, נדפיס רווח רק אם i *לא* מצביע על הארגומנט האחרון.
        // הארגומנט האחרון נמצא ב- (argc - 1)
        if (i < argc - 1) {
            printf(" ");
        }
    }
    
    // בסוף, אחרי שכל המילים הודפסו,
    // נדפיס תו של ירידת שורה ("\n")
    // כדי שהפרומפט של הטרמינל יופיע בשורה חדשה ונקייה.
    printf("\n");
    
    // נחזיר 0 כדי לסמן למערכת ההפעלה שהתוכנית הסתיימה בהצלחה
    return 0;
}